			focusConfirm: false,
			preConfirm: function () {
				return new Promise(function (resolve) {
					// Validate input
					if ($('#firstname').val() == '' || $('#lastname').val() == '' || $('#email').val() == '' || $('#phone').val() == '' || $('#company').val() == '') {
						Swal.showValidationMessage("Please enter a value in all fields"); // Show error when validation fails.
