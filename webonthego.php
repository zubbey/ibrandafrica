					} else if(!validEmail($('#email').val())){
						Swal.showValidationMessage("Please enter a valid email address"); // Show error when validation fails.
						Swal.enableButtons(); // Enable the confirm button again.
					}
					else {
						Swal.resetValidationMessage(); // Reset the validation message.
						resolve(
							{
								firstname: $('#firstname').val(),
								lastname: $('#lastname').val(),
								email: $('#email').val(),
								phone: $('#phone').val(),
								company: $('#company').val()
							}
						);
					}
				})
			},
			onOpen: function () {
				$('#firstname').focus()
			}
		}).then(function (result) {
			// If validation fails, the value is undefined. Break out here.
			if (typeof(result.value) == 'undefined') {
				return false;
			}
			payWithPaystack(result.value, amount, name);
			// swal(JSON.stringify(result))
		}).catch(swal.noop);
	})()
}

function validEmail(email) {
	var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
	return re.test(email);
};

